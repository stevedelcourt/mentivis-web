#!/usr/bin/env node
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "src", "content", "referentiel");
const TS_OUT = path.join(ROOT, "src", "data", "referentiel.ts");

const files = fs.readdirSync(CONTENT_DIR)
  .filter(f => f.endsWith(".json") && !f.endsWith(".tech.json"))
  .sort((a, b) => {
    const oa = JSON.parse(fs.readFileSync(path.join(CONTENT_DIR, a))).order || 0;
    const ob = JSON.parse(fs.readFileSync(path.join(CONTENT_DIR, b))).order || 0;
    if (oa !== ob) return oa - ob;
    // fr before en for same order
    const isEnA = a.endsWith(".en.json");
    const isEnB = b.endsWith(".en.json");
    return isEnA ? 1 : -1;
  });

const imports = files.map((f, i) => {
  const name = `a${i + 1}`;
  return `import ${name} from "../content/referentiel/${f}";`;
}).join("\n");

const refs = files.map((_, i) => `a${i + 1}`).join(", ");

const content = `/* eslint-disable */
// Auto-generated by generate-referentiel-ts.js
// Full article data with body content — used ONLY for article detail pages.
// For listings and cards, use @/data/referentiel-meta (much lighter, no body content).

import type { ReferentielArticleMeta } from "./referentiel-meta";
import { CATEGORY_LABELS } from "./referentiel-meta";
export { CATEGORY_LABELS };
${imports}

export interface ReferentielArticle extends ReferentielArticleMeta {
  content: string;
}

export const REFERENTIEL: ReferentielArticle[] = [
  ${refs},
] as ReferentielArticle[];

export function getReferentielBySlug(slug: string, lang?: string): ReferentielArticle | undefined {
  return REFERENTIEL.find((a) => a.slug === slug && (!lang || a.lang === lang));
}

export function getReferentielSlugs(): string[] {
  return REFERENTIEL.map((a) => a.slug);
}

export function getReferentielByCible(cible: string): ReferentielArticle[] {
  return REFERENTIEL.filter((a) => a.cible === cible);
}

export function getReferentielByThematique(thematique: string): ReferentielArticle[] {
  return REFERENTIEL.filter((a) => a.thematique === thematique);
}
`;

fs.writeFileSync(TS_OUT, content, "utf8");
console.log(`✓ referentiel.ts generated (${files.length} articles)`);
